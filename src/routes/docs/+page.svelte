<h2>About</h2>
<p>
	Svelte UI is a component library built from scratch for Svelte 5. Every component uses
	<code>$props()</code>, <code>$state()</code>, <code>$derived()</code>, and <code>$effect()</code>
	— no legacy <code>export let</code> syntax. Components ship with full TypeScript types,
	WCAG-compliant accessibility, and zero runtime dependencies.
</p>

<h2>Features</h2>
<p>
	<strong>Svelte 5 Runes</strong> — All components use the latest runes API for reactive props and state.
</p>
<p>
	<strong>Snippets</strong> — Replaces slots with Svelte 5 Snippets for typed, composable children.
</p>
<p>
	<strong>TypeScript</strong> — Props interfaces extend native HTML element attributes so every
	native attribute passes through automatically.
</p>
<p>
	<strong>Accessible</strong> — Focus trapping, scroll locking, ARIA roles, keyboard navigation,
	and screen reader support built in.
</p>
<p>
	<strong>Dark mode</strong> — CSS custom properties with a <code>data-theme</code> attribute
	on the root element for seamless light/dark switching.
</p>

